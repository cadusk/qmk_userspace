bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case COLEMAK:
            if (record->event.pressed) {
                default_layer_set(1UL << _COLEMAK);
                layer_move (_COLEMAK);
                keymap_config.nkro = 0;
            }
            return false;
        break;

        case QWERTY:
            if (record->event.pressed) {
                default_layer_set(1UL << _QWERTY);
                layer_move (_QWERTY);
                keymap_config.nkro = 0;
            }
            return false;
        break;
    }

    return true;
}
